    public void verusuarios(){
    ArrayList<Usuario> lista=consultasBD.consultarUsuarios();
    DefaultTableModel llenar = (DefaultTableModel) tablaUsuarios.getModel();
        Object[] fila = new Object[6];
//primero se limpia el grid
       
        for (Usuario aux : lista) {
            
            fila[0] = aux.getId_usuario();
            fila[1] = aux.getNombre();
            fila[2] = aux.getApellido();
            fila[3] = aux.getAlias();
            fila[4] = aux.getContrasena();
            fila[5] = aux.getTipo();
            if(aux.getTipo().equals("A") && usuarioActivo.getId_usuario()==aux.getId_usuario())
                admin=true;
            llenar.addRow(fila);
        }
    }
     public void LlenarMensajesAdmin(){
     ArrayList<Mensaje> lista=consultasBD.obtenerMsgAdmin();
    DefaultTableModel llenar = (DefaultTableModel) tablaMsgAdmin.getModel();
         for (int i = 0; i < llenar.getRowCount(); i++) {
             llenar.removeRow(i);
         }
         llenar.setRowCount(0);
        Object[] fila = new Object[6];
//primero se limpia el grid
       
        for (Mensaje aux : lista) {
            
            fila[0] = aux.getIdMensaje();
            fila[1] = aux.getUsuario().getNombre()+" "+aux.getUsuario().getApellido();
            fila[2] = aux.getAsunto();
            fila[3] = aux.getMensaje();
            fila[4] = aux.getTipo();
            llenar.addRow(fila);
        }
    
    }
